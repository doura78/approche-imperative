package fr.declaration.variable;

public class DeclarationApp {
    public static void main(String[] args) {
        byte a = -1;
        short b = 15000;
        int c = 1000000;
        long d = 20000L;
        float e = 99.99F;
        double f = 2.5D;
        char g = 'g';
        boolean h = true;
        String i = "Hello";
        String randomString = "Voici le résultat d’un calcul : " +
                "1+5=6";

        System.out.println(randomString);
    }
}

