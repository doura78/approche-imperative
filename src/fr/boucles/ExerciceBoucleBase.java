package fr.boucles;

public class ExerciceBoucleBase {
    // BOUCLES DE BASE
    public static void main(String[] args) {
        int i = 0;
        for (i = 0; i < 10; i++) {
            System.out.println(i);
        }

        String name = "Abdouramane Sidibé";
        for (i = 0; i < 20; i++) {
            System.out.println(name);
        }

        int j = 2;
        for (j = 0; j <= 100; j+=2) {
            System.out.println(j);
        }

        int k = 1;
        for (k= 1; k <= 99; k+=2) {
            System.out.println(k);
        }
    }
}
